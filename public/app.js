function minutesBetween(start, end = Date.now()) {
  if (!start) return "—";
  const elapsed = Math.max(0, end - new Date(start).getTime());
  const totalMinutes = Math.floor(elapsed / 60000);
  const hours = Math.floor(totalMinutes / 60);
  const minutes = totalMinutes % 60;
  if (hours > 0) return `${hours}h ${minutes}m`;
  return `${minutes}m`;
}

function formatTimestamp(start) {
  if (!start) return t("notSet");
  return new Intl.DateTimeFormat(currentLanguage, {
    month: "short",
    day: "numeric",
    hour: "numeric",
    minute: "2-digit"
  }).format(new Date(start));
}

function initials(name) {
  return name
    .split(/\s+/)
    .filter(Boolean)
    .slice(0, 2)
    .map((part) => part[0].toUpperCase())
    .join("");
}

function statusClass(status) {
  return status.toLowerCase().replace(/[^a-z0-9]+/g, "-");
}

function isOrderTakenOnlyUpdate(entry) {
  if (!entry.lastEditedBy || !entry.orderTakenBy || entry.lastEditedBy !== entry.orderTakenBy) {
    return false;
  }
  if (!entry.lastEditedAt || !entry.orderTakenAt) return false;
  const lastEditedAt = new Date(entry.lastEditedAt).getTime();
  const orderTakenAt = new Date(entry.orderTakenAt).getTime();
  return Number.isFinite(lastEditedAt) && Number.isFinite(orderTakenAt) && Math.abs(lastEditedAt - orderTakenAt) < 5000;
}

function hasRealUpdate(entry) {
  return Boolean(entry.lastEditedBy) && !isOrderTakenOnlyUpdate(entry);
}

