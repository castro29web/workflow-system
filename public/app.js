function connectEvents() {
  const isFrontDesk = Boolean(document.querySelector("#desk-list"));
  const source = new EventSource(apiUrl(isFrontDesk ? "/api/frontdesk/events" : "/api/events"));
  source.onmessage = (event) => {
    const payload = JSON.parse(event.data);
    if (isFrontDesk) {
      queue = payload.active || [];
      completedOrders = payload.completed || [];
    } else {
      queue = payload;
      completedOrders = [];
    }
    rerender();
  };
}

function setupSignin() {
  const form = document.querySelector("#signin-form");
  if (!form) return;
  const message = document.querySelector("#form-message");
  const infoStep = document.querySelector("[data-customer-info-step]");
  const categoryInputs = form.querySelectorAll("input[name='category']");
  const nameInput = form.elements.name;

  categoryInputs.forEach((input) => {
    input.addEventListener("change", () => {
      infoStep.hidden = false;
      nameInput?.focus();
    });
  });

  form.addEventListener("submit", async (event) => {
    event.preventDefault();
    if (!form.elements.category.value) {
      message.textContent = t("error");
      message.className = "form-message error";
      return;
    }

    const data = Object.fromEntries(new FormData(form));
    const customerName = String(data.name || "").trim();
    const response = await fetch(apiUrl("/api/signins"), {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(data)
    });

    if (!response.ok) {
      message.textContent = t("error");
      message.className = "form-message error";
      return;
    }

    form.reset();
    infoStep.hidden = true;
    message.textContent = "";
    message.className = "form-message";
    showSuccessScreen(customerName);
  });
}

function showSuccessScreen(name) {
  const existing = document.querySelector(".success-screen");
  if (existing) existing.remove();

  const screen = document.createElement("section");
  screen.className = "success-screen";
  screen.setAttribute("role", "status");
  screen.innerHTML = `
    <div class="success-card">
      <p class="eyebrow">${t("success")}</p>
      <h2>${escapeHtml(t("successTitle").replace("{name}", name))}</h2>
      <p>${t("successWait")}</p>
      <div class="success-bar" aria-hidden="true"><span></span></div>
    </div>
  `;

  document.body.append(screen);
  setTimeout(() => screen.remove(), 6000);
}

function setupClock() {
  const clock = document.querySelector("#clock");
  if (!clock) return;
  const tick = () => {
    clock.textContent = new Intl.DateTimeFormat(currentLanguage, {
      hour: "numeric",
      minute: "2-digit"
    }).format(new Date());
  };
  tick();
  setInterval(tick, 1000);
}

function registerServiceWorker() {
  if (!("serviceWorker" in navigator)) return;
  window.addEventListener("load", () => {
    navigator.serviceWorker.register("/sw.js").catch(() => {});
  });
}

