function renderDesk() {
  const list = document.querySelector("#desk-list");
  if (!list) return;

  if (!queue.length) {
    list.innerHTML = `
      <section class="desk-section">
        <div class="empty-state">${t("noCustomers")}</div>
      </section>
      ${renderDoneOrdersSection()}
    `;
    bindDoneOrderControls(list);
    return;
  }

  list.innerHTML = `
    <div class="table-row table-head">
      <span>${t("tableCustomer")}</span>
      <span>${t("tableCategory")}</span>
      <span>${t("tableStatus")}</span>
      <span>${t("tableStatusTime")}</span>
      <span>${t("tableActions")}</span>
    </div>
    ${queue
      .map(
        (entry) => `
          <article class="table-row">
            <div class="person-cell">
              <span class="avatar">${initials(entry.name)}</span>
              <div>
                <div class="customer-name-tools">
                  <small class="signed-in-time">${t("signedIn")}: ${formatTimestamp(entry.signedInAt)}</small>
                  <button type="button" data-toggle-details="${entry.id}" class="inline-detail-toggle">
                    ${openDetailIds.has(entry.id) ? t("hideDetails") : t("showDetails")}
                  </button>
                </div>
                <strong>${escapeHtml(entry.name)}</strong>
                <small>${escapeHtml(entry.company || t("noCompany"))}</small>
                ${
                  entry.comments
                    ? `<small class="comment-preview">${t("commentPrefix")}: ${escapeHtml(entry.comments)}</small>`
                    : ""
                }
                ${
                  entry.removalRequired
                    ? `<small class="comment-preview">${t("removalRequired")}</small>`
                    : ""
                }
                ${
                  entry.status === "Ready"
                    ? `<small class="quality-check-hint">${t("qualityCheck")}</small>`
                    : ""
                }
                ${
                  hasRealUpdate(entry)
                    ? `<small class="comment-preview">${t("updatedBy")} ${escapeHtml(entry.lastEditedBy)}</small>`
                    : ""
                }
                ${
                  entry.orderTakenBy
                    ? `<small class="comment-preview">${t("orderTakenBy")} ${escapeHtml(entry.orderTakenBy)}</small>`
                    : ""
                }
              </div>
            </div>
            <span>${escapeHtml(displayCategory(entry.category))}</span>
            <span><mark class="status ${statusClass(entry.status)}">${displayStatus(entry.status)}</mark></span>
            <span>${minutesBetween(entry.statusChangedAt)}</span>
            <div class="action-row">
              ${
                entry.status === "Waiting" && !entry.orderTakenBy
                  ? `<button data-status="Order Taken" data-id="${entry.id}">${t("takeOrder")}</button>`
                  : entry.status === "Ready"
                  ? `
                    <button data-status="Complete" data-id="${entry.id}" class="finalize-button">${t("finalizeOrder")}</button>
                    <button type="button" data-toggle-comment="${entry.id}" class="quiet ${entry.comments ? "has-comment" : ""}">
                      ${t("comments")}
                    </button>
                  `
                  : `
                    <div class="job-toggle" role="group" aria-label="${t("jobTypeLabel")}">
                      <button data-status="To-Go" data-id="${entry.id}" class="${entry.status === "To-Go" ? "active" : ""}">
                        ${t("toGo")}
                      </button>
                      <button data-status="Install" data-id="${entry.id}" class="${entry.status === "Install" ? "active" : ""}">
                        ${t("install")}
                      </button>
                    </div>
                    <button type="button" data-toggle-comment="${entry.id}" class="quiet ${entry.comments ? "has-comment" : ""}">
                      ${t("comments")}
                    </button>
                  `
              }
            </div>
            ${
              openDetailIds.has(entry.id)
                ? `
                  <div class="customer-details-panel">
                    <div>
                      <span>${t("phone")}</span>
                      <strong>${escapeHtml(entry.phone || t("notSet"))}</strong>
                    </div>
                    <label>
                      <span>${t("company")}</span>
                      <input data-contact-field="company" data-id="${entry.id}" value="${escapeHtml(
                        contactDraftFor(entry).company
                      )}" placeholder="${t("addCompany")}" />
                    </label>
                    <label>
                      <span>${t("email")}</span>
                      <input data-contact-field="email" data-id="${entry.id}" type="email" value="${escapeHtml(
                        contactDraftFor(entry).email
                      )}" placeholder="${t("addEmail")}" />
                    </label>
                    <div class="details-actions">
                      <button type="button" data-save-contact="${entry.id}">${t("saveDetails")}</button>
                      ${
                        entry.company
                          ? `<button type="button" class="quiet" data-clear-contact="company" data-id="${entry.id}">${t("clearCompany")}</button>`
                          : ""
                      }
                      ${
                        entry.email
                          ? `<button type="button" class="quiet" data-clear-contact="email" data-id="${entry.id}">${t("clearEmail")}</button>`
                          : ""
                      }
                    </div>
                  </div>
                `
                : ""
            }
            ${
              openCommentIds.has(entry.id)
                ? `
                  <div class="comment-panel">
                    <label>
                      <span>${t("installerComments")}</span>
                      <textarea data-comment-input="${entry.id}" rows="3">${escapeHtml(
                        commentDrafts.get(entry.id) ?? entry.comments ?? ""
                      )}</textarea>
                    </label>
                    <button type="button" data-save-comment="${entry.id}">${t("saveComment")}</button>
                  </div>
                `
                : ""
            }
          </article>
        `
      )
      .join("")}
  `;

  list.insertAdjacentHTML(
    "beforeend",
    renderDoneOrdersSection()
  );

  bindDoneOrderControls(list);

  list.querySelectorAll("button[data-status]").forEach((button) => {
    button.addEventListener("click", async () => {
      const id = button.dataset.id;
      const status = button.dataset.status;
      const entry = queue.find((item) => item.id === id);
      if (status === "Order Taken") {
        const orderTakenBy = await showOrderTakerPrompt(entry?.name || "this customer");
        await updateStatus(id, status, {
          removalRequired: false,
          orderTakenBy
        });
        return;
      }
      const staffName = await requireStaffName();
      if (status === "Install") {
        const removalRequired = await showRemovalPrompt(entry?.name || "this customer");
        openCommentReminder(id);
        await updateStatus(id, status, { removalRequired, staffName });
        renderDesk();
        return;
      }
      if (status === "To-Go") {
        openCommentReminder(id);
        await updateStatus(id, status, { removalRequired: false, staffName });
        renderDesk();
        return;
      }
      await updateStatus(id, status, { staffName });
    });
  });

  list.querySelectorAll("button[data-toggle-details]").forEach((button) => {
    button.addEventListener("click", () => {
      const id = button.dataset.toggleDetails;
      if (openDetailIds.has(id)) {
        openDetailIds.delete(id);
      } else {
        openDetailIds.add(id);
      }
      renderDesk();
    });
  });

  list.querySelectorAll("button[data-toggle-comment]").forEach((button) => {
    button.addEventListener("click", async () => {
      await requireStaffName();
      const id = button.dataset.toggleComment;
      if (openCommentIds.has(id)) {
        openCommentIds.delete(id);
        pendingCommentFocusId = "";
      } else {
        openCommentIds.add(id);
        pendingCommentFocusId = id;
      }
      renderDesk();
    });
  });

  list.querySelectorAll("input[data-contact-field]").forEach((input) => {
    input.addEventListener("input", () => {
      const id = input.dataset.id;
      const entry = queue.find((item) => item.id === id);
      const draft = contactDraftFor(entry || { id, company: "", email: "" });
      draft[input.dataset.contactField] = input.value;
      contactDrafts.set(id, draft);
    });
  });

  list.querySelectorAll("button[data-save-contact]").forEach((button) => {
    button.addEventListener("click", async () => {
      const id = button.dataset.saveContact;
      const company = list.querySelector(`input[data-id="${id}"][data-contact-field="company"]`)?.value || "";
      const email = list.querySelector(`input[data-id="${id}"][data-contact-field="email"]`)?.value || "";
      const staffName = await requireStaffName();
      await updateContact(id, company, email, staffName);
    });
  });

  list.querySelectorAll("button[data-clear-contact]").forEach((button) => {
    button.addEventListener("click", async () => {
      const id = button.dataset.id;
      const entry = queue.find((item) => item.id === id);
      const draft = contactDraftFor(entry || { id, company: "", email: "" });
      draft[button.dataset.clearContact] = "";
      contactDrafts.set(id, draft);
      const staffName = await requireStaffName();
      await updateContact(id, draft.company, draft.email, staffName);
    });
  });

  list.querySelectorAll("textarea[data-comment-input]").forEach((textarea) => {
    textarea.addEventListener("input", () => {
      commentDrafts.set(textarea.dataset.commentInput, textarea.value);
    });
  });

  list.querySelectorAll("button[data-save-comment]").forEach((button) => {
    button.addEventListener("click", async () => {
      const id = button.dataset.saveComment;
      const textarea = list.querySelector(`textarea[data-comment-input="${id}"]`);
      const staffName = await requireStaffName();
      updateComments(id, textarea?.value || "", staffName);
    });
  });

  if (pendingCommentFocusId) {
    const textarea = list.querySelector(`textarea[data-comment-input="${pendingCommentFocusId}"]`);
    textarea?.focus();
    textarea?.setSelectionRange(textarea.value.length, textarea.value.length);
    pendingCommentFocusId = "";
  }
}

