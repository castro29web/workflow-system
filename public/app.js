async function checkSession() {
  if (hasRememberedAuth()) return true;
  const response = await fetch(apiUrl("/api/session"), { cache: "no-store" });
  if (!response.ok) return false;
  const session = await response.json();
  if (session.authenticated) rememberAuth();
  return session.authenticated;
}

function showPinPrompt({ locked = false, destination = "" } = {}) {
  return new Promise((resolve) => {
    const existing = document.querySelector(".pin-overlay");
    if (existing) existing.remove();

    const overlay = document.createElement("div");
    overlay.className = "pin-overlay";
    overlay.innerHTML = `
      <form class="pin-card">
        <p class="eyebrow">${t("userLogin")}</p>
        <h2>${t("enterAccessCode")}</h2>
        <label>
          <span>${t("code")}</span>
          <input name="pin" inputmode="numeric" autocomplete="one-time-code" maxlength="8" required />
        </label>
        <p class="pin-error" role="alert"></p>
        <div class="pin-actions">
          ${locked ? "" : `<button type="button" class="secondary-action" data-cancel>${t("cancel")}</button>`}
          <button type="submit" class="primary-action">${t("logIn")}</button>
        </div>
      </form>
    `;

    document.body.append(overlay);
    document.body.classList.toggle("auth-lock", locked);

    const form = overlay.querySelector("form");
    const input = overlay.querySelector("input");
    const error = overlay.querySelector(".pin-error");
    const cancel = overlay.querySelector("[data-cancel]");

    input.focus();

    cancel?.addEventListener("click", () => {
      overlay.remove();
      resolve(false);
    });

    form.addEventListener("submit", async (event) => {
      event.preventDefault();
      error.textContent = "";

      const response = await fetch(apiUrl("/api/login"), {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ pin: input.value.trim() })
      });

      if (!response.ok) {
        forgetAuth();
        error.textContent = t("incorrectCode");
        input.select();
        return;
      }

      rememberAuth();
      overlay.remove();
      document.body.classList.remove("auth-lock");
      resolve(true);
      if (destination) window.location.href = destination;
    });
  });
}

