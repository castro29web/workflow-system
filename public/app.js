function doneOrderTimestamp(entry) {
  const timestamp = new Date(entry.completedAt || entry.statusChangedAt || entry.signedInAt).getTime();
  return Number.isFinite(timestamp) ? timestamp : 0;
}

function filteredDoneOrders() {
  if (doneOrdersView === "hidden") return [];
  if (doneOrdersView === "all") return completedOrders;
  if (doneOrdersView === "today") {
    const start = new Date();
    start.setHours(8, 0, 0, 0);
    const end = new Date();
    end.setHours(20, 0, 0, 0);
    return completedOrders.filter((entry) => {
      const timestamp = doneOrderTimestamp(entry);
      return timestamp >= start.getTime() && timestamp <= end.getTime();
    });
  }

  const days = Number(doneOrdersView);
  if (!Number.isFinite(days)) return [];
  const cutoff = Date.now() - days * 24 * 60 * 60 * 1000;
  return completedOrders.filter((entry) => doneOrderTimestamp(entry) >= cutoff);
}

