function rememberAuth() {
  sessionStorage.setItem(authKey, "true");
}

function forgetAuth() {
  sessionStorage.removeItem(authKey);
}

function hasRememberedAuth() {
  return sessionStorage.getItem(authKey) === "true";
}

function escapeHtml(value) {
  return String(value ?? "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function t(key) {
  return translations[currentLanguage]?.[key] || translations.en[key] || key;
}

function applyLanguage(nextLanguage = currentLanguage) {
  currentLanguage = englishOnlyPage ? "en" : nextLanguage;
  if (!englishOnlyPage) localStorage.setItem(languageKey, currentLanguage);
  document.documentElement.lang = currentLanguage;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });
  document.querySelectorAll(".language-option").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === currentLanguage);
  });
  rerender();
}

function setupLanguageControls() {
  document.querySelectorAll(".language-option").forEach((button) => {
    button.addEventListener("click", () => applyLanguage(button.dataset.lang));
  });
  applyLanguage(currentLanguage);
}

